                  <p>Camoufler une cicatrice en y apposant un tatouage encore moins discret : la démarche peut paraître paradoxale mais permet de se réapproprier son corps tout en le sublimant. Une tendance qui attire de plus en plus de monde, grâce à la démocratisation de cette forme d’art longtemps dévoyée.

Contrairement au tatouage purement réparateur, dont l’objectif est de se faire oublier, de paraître aussi naturel que la peau elle-même, le tatouage décoratif traditionnel utilisé pour camoufler une cicatrice aura pour but de détourner le regard, en jouant sur l’effet d’optique. Il permet donc de changer le regard des autres mais aussi de modifier son regard sur son propre corps, car l’on passe d’une particularité physique subie (et souvent synonyme de traumatisme) à une réappropriation active de son corps.

En pratique, il n'y a pas de risque à se faire tatouer sur une peau claire. C'est exactement le même principe. On inclut du pigment dans le derme.
Que la peau soit claire ou mate, il n'y a pas de problème différent. En revanche, les tatoueurs évitent de tatouer les grains de beauté a fortiori si les tatoués sont de grande taille.

Sur un plan clinique, dépister un naevus ou un grain de beauté en train de se transformer en lésion cancéreuse est moins évident chez un tatoué. Car en dermatologie, l'inclusion de pigments étrangers au sein du grain de beauté posera des problèmes pour la surveillance des naevus.</p>
